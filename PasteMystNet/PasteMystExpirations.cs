namespace PasteMystNet
{

    /// <summary>
    /// This class contains pre-defined expiration signatures.
    /// </summary>
    public static class PasteMystExpirations
    {

        /// <summary>The paste will never expire.</summary>
        public const string Never = "never";

        /// <summary>The paste will expire in one hour.</summary>
        public const string OneHour = "1h";

        /// <summary>The paste will expire in two hours.</summary>
        public const string TwoHours = "2h";

        /// <summary>The paste will expire in ten hours.</summary>
        public const string TenHours = "10h";

        /// <summary>The paste will expire in one day.</summary>
        public const string OneDay = "1d";

        /// <summary>The paste will expire in two days.</summary>
        public const string TwoDays = "2d";

        /// <summary>The paste will expire in one week.</summary>
        public const string OneWeek = "1w";

        /// <summary>The paste will expire in one month.</summary>
        public const string OneMonth = "1m";

        /// <summary>The paste will expire in one year.</summary>
        public const string OneYear = "1y";

    }

}